    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!(txtCodigo.getText().equals("") || txtNombre.getText().equals("") || txtEstado.getText().equals("") || txtResolucion.getText().equals("") || txtTipoEvidencia.getText().equals(""))) {

            Evidencia objA = new Evidencia();

            objA.setCodigo(txtCodigo.getText().trim());
            objA.setNombre(txtNombre.getText().trim());
            objA.setEstado(txtEstado.getText().trim());
            objA.setResolucion(txtResolucion.getText().trim());
            objA.setTipo_Evidencia(txtTipoEvidencia.getText().trim());
            objA.setCedula(jComboCedula.getSelectedItem().toString());
            objA.setId_Codigo(Jcodigo.getSelectedItem().toString());
            if (new IngEvidencia().Guardar(objA)) {
                JOptionPane.showMessageDialog(null, "Ingresado correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "NO ingresado");
            }
            
        };
        txtCodigo.setText("");
        txtEstado.setText("");
        txtNombre.setText("");
        txtResolucion.setText("");
        txtTipoEvidencia.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

