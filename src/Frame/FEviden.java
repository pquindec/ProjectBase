public void CargarCed() throws SQLException  {
        Conexion conexion = new Conexion();
        Connection cn = conexion.getConexion();
        String sql = "SELECT * FROM Usuario";

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                jComboCedula.addItem(rs.getString("txtCedula"));
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Problema con la consulta");
        } finally {
          
                cn.close();
        }
    }
public void Cargarcodigo() throws SQLException  {
        Conexion conexion = new Conexion();
        Connection cn = conexion.getConexion();
        String sql = "SELECT * FROM tCriterio";

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Jcodigo.addItem(rs.getString("id_criterio"));
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Problema con la consulta");
        } finally {
          
                cn.close();
        }
    }
